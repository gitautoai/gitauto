import re

from utils.error.handle_exceptions import handle_exceptions


@handle_exceptions(default_return_value=lambda log: log, raise_on_error=False)
def strip_node_modules_from_stacktrace(log: str):
    """Remove node_modules stack frames from error logs. Keep only user-code frames."""
    if not log:
        return log

    lines = log.split("\n")
    result: list[str] = []
    for line in lines:
        # Stack frame lines look like: "      at funcName (node_modules/...)"
        # or "      at node_modules/..."
        if re.match(r"^\s+at\s.*node_modules/", line):
            continue
        result.append(line)

    return "\n".join(result)
