from utils.logs.strip_node_modules_from_stacktrace import (
    strip_node_modules_from_stacktrace,
)


def test_strip_node_modules_removes_node_modules_frames():
    log = """    TypeError: Cannot read properties of undefined

      at Account (src/pages/Account/index.tsx:36:63)
      at renderWithHooks (node_modules/react-dom/cjs/react-dom.development.js:14985:18)
      at mountIndeterminateComponent (node_modules/react-dom/cjs/react-dom.development.js:17811:13)
      at renderAccount (test/pages/Account/index.test.tsx:354:16)"""

    result = strip_node_modules_from_stacktrace(log)
    assert "node_modules" not in result
    assert "at Account (src/pages/Account/index.tsx:36:63)" in result
    assert "at renderAccount (test/pages/Account/index.test.tsx:354:16)" in result


def test_strip_node_modules_preserves_non_stacktrace_lines():
    log = """FAIL test/pages/Account/index.test.tsx (26.865 s)
  ● Test name

    TypeError: message

      at Account (src/pages/Account/index.tsx:36:63)
      at renderWithHooks (node_modules/react-dom/cjs/react-dom.development.js:14985:18)

Test Suites: 1 failed, 1 total"""

    result = strip_node_modules_from_stacktrace(log)
    assert "FAIL test/pages/Account" in result
    assert "● Test name" in result
    assert "TypeError: message" in result
    assert "Test Suites: 1 failed" in result
    assert "node_modules" not in result


def test_strip_node_modules_handles_empty():
    assert strip_node_modules_from_stacktrace("") == ""


def test_strip_node_modules_no_node_modules():
    log = "at Account (src/pages/Account/index.tsx:36:63)"
    assert strip_node_modules_from_stacktrace(log) == log
